#include "aux.h"

void	put_int(int n)
{
	char	nb;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n >= 10)
		{
			put_int(n / 10);
		}
		nb = n % 10 + 48;
		write(1, &nb, 1);
	}
}
