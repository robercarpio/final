#include "aux.h"

int	fill_right(char **map, int y, int x)
{
	while (x < line_len(map[y]) && map[y][x + 1] != '1')
	{
		if (map[y][x + 1] != 'E')
			return (1);
		map[y][x + 1] = 'V';
		x++;
	}
	return (0);
}
