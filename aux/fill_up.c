#include "aux.h"

int	fill_up(char **map, int y, int x)
{
	while (y > 0 && map[y - 1][x] != '1')
	{
		if (map[y -1][x] != 'E')
			return (1);
		map[y - 1][x] = 'V';
		y--;
	}
	return (0);
}
