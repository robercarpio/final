#include "aux.h"

int	fill_left(char **map, int y, int x)
{
	while (x > 0 && map[y][x - 1] != '1')
	{
		if (map[y][x - 1] != 'E')
			return (1);
		map[y][x - 1] = 'V';
		x--;
	}
	return (0);
}
